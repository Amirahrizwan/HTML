a = True
b = False

# Using 'and' operator
print("a and b:", a and b)  # True only if both are True

# Using 'or' operator
print("a or b:", a or b)    # True if any one is True

# Example with numbers
x = 10
y = 5

if x > 0 and y > 0:
    print("Both numbers are positive")

if x > 0 or y < 0:
    print("At least one condition is True")
