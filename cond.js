

// if
let age = 18;
if (age >= 18) {
  console.log("Adult");
}


// if else
let score = 40;
if (score >= 50) {
  console.log("Pass");
} else {
  console.log("Fail");
}


// if else if
let marks = 75;
if (marks >= 90) {
  console.log("A+");
} else if (marks >= 75) {
  console.log("A");
} else {
  console.log("B");
}

// switch
let day = "Monday";
switch (day) {
  case "Monday":
    console.log("Start of week");
    break;
  case "Friday":
    console.log("Almost weekend");
    break;
  default:
    console.log("Regular day");
}
