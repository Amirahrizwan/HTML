# Integer
a = 10
print("a =", a, "| Type:", type(a))

# Float
b = 3.14
print("b =", b, "| Type:", type(b))

# String
c = "Hello"
print("c =", c, "| Type:", type(c))

# Boolean
d = True
print("d =", d, "| Type:", type(d))

# List
e = [1, 2, 3]
print("e =", e, "| Type:", type(e))

# Tuple
f = (4, 5, 6)
print("f =", f, "| Type:", type(f))

# Set
g = {7, 8, 9}
print("g =", g, "| Type:", type(g))

# Dictionary
h = {"name": "Alice", "age": 25}
print("h =", h, "| Type:", type(h))
